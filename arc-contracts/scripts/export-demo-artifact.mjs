/**
 * export-demo-artifact.mjs
 *
 * Run this ONCE after `npx hardhat compile`:
 *
 *   cd arc-contracts
 *   npx hardhat compile
 *   node scripts/export-demo-artifact.mjs
 *
 * Copies the compiled TreasuryRebalancerDemo artifact (ABI + bytecode)
 * into the Next.js dashboard so Vercel can import it at build time.
 * Commit the output file to git — Vercel needs it!
 */

import fs   from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __filename = fileURLToPath(import.meta.url);
const __dirname  = path.dirname(__filename);

const ARTIFACT_SRC  = path.join(
  __dirname, '..', 'artifacts', 'contracts',
  'TreasuryRebalancerDemo.sol', 'TreasuryRebalancerDemo.json'
);

const ARTIFACT_DEST = path.join(
  __dirname, '..', '..', 'genrebalancer-dashboard',
  'src', 'contracts', 'TreasuryRebalancerDemo.json'
);

// ── Check source exists ────────────────────────────────────────────────────────
if (!fs.existsSync(ARTIFACT_SRC)) {
  console.error('❌  Artifact not found:', ARTIFACT_SRC);
  console.error('   Run `npx hardhat compile` first, then retry.\n');
  process.exit(1);
}

// ── Read Hardhat artifact ─────────────────────────────────────────────────────
const hardhatArtifact = JSON.parse(fs.readFileSync(ARTIFACT_SRC, 'utf8'));

// We only need abi and bytecode — keep the file small
const minimal = {
  _note:    'Auto-generated by arc-contracts/scripts/export-demo-artifact.mjs — DO NOT EDIT MANUALLY',
  abi:      hardhatArtifact.abi,
  bytecode: hardhatArtifact.bytecode,
};

// ── Ensure destination directory exists ──────────────────────────────────────
const destDir = path.dirname(ARTIFACT_DEST);
if (!fs.existsSync(destDir)) {
  fs.mkdirSync(destDir, { recursive: true });
  console.log('📁  Created directory:', destDir);
}

// ── Write ─────────────────────────────────────────────────────────────────────
fs.writeFileSync(ARTIFACT_DEST, JSON.stringify(minimal, null, 2), 'utf8');

console.log('✅  Artifact exported successfully!');
console.log('   Source :', ARTIFACT_SRC);
console.log('   Output :', ARTIFACT_DEST);
console.log('');
console.log('📌  Next steps:');
console.log('   1. git add genrebalancer-dashboard/src/contracts/TreasuryRebalancerDemo.json');
console.log('   2. git commit -m "chore: add compiled TreasuryRebalancerDemo artifact"');
console.log('   3. git push  →  Vercel auto-deploys');
